import 'package:bloc/bloc.dart';
import 'package:flutter/material.dart';

part 'onboarding_event.dart';
part 'onboarding_state.dart';

class OnboardingBloc extends Bloc<OnboardingEvent, OnboardingState> {
  int pageIndex = 0;
  final pageController = PageController(initialPage: 0);

  OnboardingBloc() : super(OnboardingInitial()) {
    on<PageSwipedEvent>((event, emit) {
      pageIndex = event.index;
      emit(PageChangedState(counter: pageIndex));
      print("ðŸ”¹ PageSwipedEvent: Nouvelle page index = $pageIndex");
    });

    on<PageChangedEvent>((event, emit) {
      print("ðŸ›  PageChangedEvent reÃ§u, pageIndex actuel = $pageIndex");

      if (pageIndex == 2) {
        print("âœ… DerniÃ¨re page atteinte, passage automatique Ã  HomePage !");
        emit(NextScreenState());
        return;
      }

      pageIndex += 1;
      pageController.animateToPage(
        pageIndex,
        duration: Duration(milliseconds: 500),
        curve: Curves.ease,
      );

      emit(PageChangedState(counter: pageIndex));
      print("âœ… PageChangedState Ã©mis avec pageIndex = $pageIndex");
    });
  }
}
